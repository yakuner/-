<template>
    <div class="col-md-4">
        <form class="form-horizontal">
        <div class="form-group">
            <label>用户名</label>
            <input type="text" class="form-control" placeholder="用户名" v-model="userName">
        </div>
        <div class="form-group">
            <label>评论内容</label>
            <textarea class="form-control" rows="6" placeholder="评论内容" v-model="centent"></textarea>
        </div>
        <div class="form-group">
            <div class="col-sm-offset-2 col-sm-10">
            <button type="button" class="btn btn-default pull-right" @click="addC">提交</button>
            </div>
        </div>
        </form>
    </div>
</template>

<script>
export default {
  name: '',
  props:['addComment'],
  data(){
    return{
      userName:'',
      centent:''
    }
  },
  methods:{
    addC(){
      // 获取用户输入内容
      const {userName,centent} = this
      // 将非空校验后的数据添加成一个对象
      let id = Date.now();
      let newComment;
      userName.trim() && centent.trim() ? newComment = {id,userName,centent} : alert('请检查输入的内容')
      this.addComment(newComment)
      // 将对象当成实参传回父组件
    }
  }
  
}
</script>

